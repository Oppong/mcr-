        <h6 class="font-weight-bold mt-4"> <em>Introduction</em> </h6>
        <p class="">The word ‘entrepreneur’ has variously been defined in entrepreneurship writings
           and this author has adopted the one given by Scarborough: ‘An entrepreneur is one who creates a new
            business in the face of risk and uncertainty for the purpose of achieving profit and growth by
            identifying significant opportunities and assembling the necessary resources to capitalize on
            them’(Scarborough,20132, p.21). </p>

           <p>This definition offers a knowledge that, a prospective entrepreneur
           must go through some form of systematic processes and activities to establish a business, grow and
           ensure its sustainability. Scarborough (2013) observes that, many people come out with great
          ideas but most of them never act on them. To him, ‘Entrepreneurs do’. How then do they do?</p>

          <p>This article proceeds on the <em>‘Entrepreneurs do’</em>  ideology which the author otherwise states as
            ‘Entrepreneurs are doing-dreamers’. That is, prospective entrepreneurs do not just wish to become,
            they act to become. The actions can be categorized under six steps, namely:  </p>
          <ul>
            <li>Step1: Acquire Basic Financial Knowledge </li>
            <li>Step2: Create Enabling Business Mindset  </li>
            <li>Step3: Develop Business Ideas  </li>
            <li>Step4: Prepare Business Concept/Proposal  </li>
            <li>Step5: Establish and Start a Business  </li>
            <li>Step6: Grow and Sustain the Business  </li>
          </ul>

          <p>The articles on these Steps are presented separately on this MCRC platform in the form of series - Step1, Step2, Step3, Step4, Step5 and Step6. They are based on data
            collected quantitatively and qualitatively during personal life development presentations.  </p>

            <p>Your contributions, arguments and questions are welcomed. You may further contact the author through the MCRC for personal or group engagements,
              particularly on ensuring controls in businesses and personal life development. </p>

            <p>Remember: <em> <strong> “Prospective entrepreneurs do not just wish to become, they act to become”.</strong> </em> </p>

