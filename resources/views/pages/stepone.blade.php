        </ul>
    </li>
    </ul>


    <p> <u> <strong>Knowing</strong> </u> the potential sources of your finance. </p>
    <ul>
      <li>Parents/Relative</li>
      <li>Employment – Employed, Self-Employed</li>
      <li>Savings</li>
      <li>Investment</li>
      <li>Loan</li>
      <li>Windfall/Donations</li>
