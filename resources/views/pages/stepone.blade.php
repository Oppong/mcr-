        <p class="">This short article is one of six series under the topic: <em> <strong> Step-By-Step Entry into the World of the Entrepreneur. </strong></em>
           Its objective is to encourage people to pursue financial knowledge as a primary step into the world
           of entrepreneur. One of the awareness often created in entrepreneurship presentations is that, getting
            a business idea first is of primary importance to a prospective entrepreneur than the money to start it.
             Among others, getting a business idea first provides good basis for estimating how much you would need,
             determining where to source for funding and justifying the need to the financier(s) which can be a friend(s),
              parent(s), money lender(s) or from one’s personal investment(s). However, this must not rule out the
               importance of having some basic financial knowledge. After all, the ultimate goal of entrepreneurship
               is generally to create wealth, which has the bigger economic picture than just the money needed to
                start a business. Financial knowledge is critical at all times-before, during and after establishing
                a business. </p> <br>

<p>Ideally, a prospective entrepreneur must have some basic financial knowledge -
  personal finances and management behaviour, the constituents of finance and the
  financial sector products, opportunities and basic issues worth noting – savings,
  investment, lending, financial instruments and others. Such worth of knowledge and
  mindfulness for instance enable a person, such as a student, a jobless or an employee
  to establish strategic relationships with individuals and entities far before the time
  he/she ventures into entrepreneurship and have to access credit and other opportunities. </p> <br>

<p>Acquiring basic financial knowledge can start from personal conduct of financial inventory through
  completion of worksheets such as below (downloadable version attached as Appendix ‘A’):</p> <br>


