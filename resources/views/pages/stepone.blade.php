<p> <u> <strong>Acquiring</strong> </u> the funds after careful consideration of the identified sources. </p>
<ul>
  <li>What specific thing(s) do I need the money for?</li>
  <li>Which source do I choose and why?</li>
  <li>What is the best arrangement or conditions for this fund?</li>
  <li>What payment plan do I have?</li>
  <li>Note: Do not borrow money or access a loan if you do not have a specific <br>
     need for it, even if it is interest free, and a plan for the payment.</li>
  <li>Etc.</li>
</ul>

