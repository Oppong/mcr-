<h6 class="font-weight-bold text-center"> Financial inventory exercise </h6>
<p>The exercise is not to be done by only people who earn income. Whether one is earning income or not,
  he/she must complete it. You would realize that, one of the importance of
  financial inventory exercise is its potential for prompting people to create business. </p>

  <p>The result of the inventory will indicate either positive or negative net income (i.e. income minus expenditure).
    The following questions would then be answered to give relevance to the exercise:</p>

  <ol>
    <li>Do you have a positive net income? If yes, what do you do with it?</li>
    <li>Do you have a negative net income? If yes, how do you fill this gap?</li>
    <li>Have you done such an exercise about your finances before?</li>
    <li>What have you learned from this exercise?</li>
    <li>What expenditure items do you find relevant or otherwise to maintain or introduce or delete?</li>
    <li>Do you need to change your financial management practices/lifestyle?</li>
  </ol>

<br>

<p>In all the surveys conducted with this inventory worksheet at the Author’s presentations
   (seminars and the like), over 90% of participants obtain negative net income.
   They mostly include students, jobless, petty traders (table-top traders) and
    small scale enterprises (SMEs) owners. There are instances where some people
    shed tears for realizing that, they could have gathered enough to go into business,
    get out of debt or improve their businesses long ago if they had disciplined themselves
    financially a little. </p>

    <br>

    <p>
      You will recall Scarborough’s assertion that ‘Many people come out with great ideas but most
      of them never act on them’ Scarborough (2013). It has been found from discussions at our
      seminars that, if students, the jobless and the masses would be educated in personal
      financial management as an initial critical step to go into the world of entrepreneur,
      this abandonment of great business ideas will be appreciably addressed. Going into business
      with one’s own fund/capital or interest-free loan is mostly the preferred option in some
      socio-cultural environments. We therefore strongly recommend that, topics treated at entrepreneurship
       seminars and the like include ‘acquisition of basic financial management knowledge’ as a critical module
        of primary concern in entrepreneurship.
    </p>

    <br>

    <h6 class="font-weight-bold text-center"> Factors accounting for financial indiscipline </h6>
    <p>We have also identified the following as some of the factors accounting for financial indiscipline.
      They may help those who complete the inventory worksheet to review their results for the needed
      change in their financial management practices/lifestyle or behaviour.</p>

    <ol type="i">
        <li>No financial plan just as there may be no life plans.</li>
        <li>Haphazard spending</li>
        <li>Unnecessary purchases</li>
        <li>Unbudgeted for financial commitments.</li>
        <li>Disregard for lower currency denominations</li>
        <li>Disregard for value for money considerations in purchases.</li>
        <li>Excessive accumulation of wealth in clothing/unproductive things.</li>
        <li>Lack of knowledge about where to invest. </li>
        <li>Lack of knowledge about how to invest.</li>
        <li>No conscious periodic review of personal finances. </li>
        <li>No separation between personal and business finances.</li>
        <li>Feeding the monkeys. Guess what? </li>
    </ol>

    <br>

    <h6 class="font-weight-bold text-center"> Good habit to cultivate  </h6>
    <p>We have further identified the following as some of the good habits to cultivate in managing one’s finances.</p>

    <p> <u> <strong>	Understanding</strong> </u> what constitute finance:   </p>
    <ul>
      <li>Economics</li>
      <li>Business</li>
      <li>Money</li>
