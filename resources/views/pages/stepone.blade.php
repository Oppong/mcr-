<h6 class="font-weight-bold text-center"> Care to know about the financial sector  </h6>
<p>Prospective entrepreneurs must care about having some basic knowledge about their Country’s
  financial sector – Bank and non-bank financial institutions, fund management institutions, the
  stock market, payment systems, interest rates, treasury bills, fixed deposits, financial regulators,
  licensing issues (legal and illegal operators) and many more. There are a lot of information which one can obtain with a little effort. </p>

<br>

<p>
  We have found that, a good number of people lack knowledge about financial matters and their related institutions. This situation has potential for some people becoming victims of illegal financial operators and high cost of financing business as starters, particularly when there are unfortunate information failures on the part of state institutions. Generally, it is possible for one to just walk to financial institutions and collect brochures about financial products, just to educate him/herself even before becoming an entrepreneur. The websites of the institutions, the central banks and other financial sector regulators can also be of great help. The daily print and electronic news also have a lot to offer but some people pay less or no attention to them. They even change stations or switch off their television and radio sets when it comes to financial news items. Likewise, they flip over the financial news pages of newspapers and also do not see the need to read or buy dedicated financial newspapers.
</p>

<br>

<p>
  There are many other issues to talk about but we would touch just one more finding. This is about opening and operating a bank account as a prospective entrepreneur. A number of people testify that, their mere having of a bank account encouraged their relatives and financiers to offer them money. In most cases, financial sources make it a requirement for one to open a bank account before they give the money, be it the banks, the uncles, brothers and sisters. It would therefore be better for prospective entrepreneurs to have knowledge about account opening and operations. This knowledge will facilitate their future company account opening and operation.
</p>

<br>

<p>
  It is hoped that, the primary importance of acquiring basic financial knowledge as one enters the world of the entrepreneur would be appreciated upon the little said in this article
</p>

