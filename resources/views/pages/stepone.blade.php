      <li>Funding</li>
      <li>Time etc.</li>
    </ul>

    <br>

    <p> <u> <strong>Analyzing</strong> </u> to know your financial needs and plan for them.   </p>
    <ul>
        <li>Present Needs <ul>
          <li>Daily and monthly needs </li>
        </ul>
    </li>
    </ul>
    <p>(Refer to the inventory sheet)</p>

    <ul>
        <li>Future Needs: <ul>
          <li>Food, clothing and shelter.</li>
          <li>Education</li>
          <li>Business</li>
