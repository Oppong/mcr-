<p> <u> <strong>Monitor & Evaluate </strong> </u> your spending, debt servicing and other financial considerations. </p>
<ul>
  <li>Pay yourself (1).</li>
  <li>Pay your business (2), if applicable.</li>
  <li>Have a reserve money/fund (3).</li>
  <li>Save and Invest (4) – Treasury bill, fixed deposit, managed fund, etc.     </li>
