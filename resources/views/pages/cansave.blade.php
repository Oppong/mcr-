          <p>From the table 1, assuming Messrs. <strong>A</strong>  and <strong>B</strong> are in an economy where a person will die after the 5th and 6th years
            if he/she lives on a yearly income of $50 and $60 respectively.  </p>

            <br>

          <ul>
            <li>Mr. <strong>A</strong>  lives on his full yearly earnings of $50 and had to die at the beginning of the 6th year. </li>
            <li>Mr. <strong>B</strong> chooses to live like Mr. A and saved and invested $10 every year and made  $60 + Investment Income
              to extend his live beyond the 6th year and some more.  </li>
            <li>If Mr. <strong>A</strong> had also chosen to live like someone who earns $40 and saved and invested $10, he could have had
              $50 + Investment Income to extend his life beyond the 5th year and some more.  </li>

          </ul>

            <br>

            <p> Message: Don’t spend all because you have. You can save and invest if you choose to live like someone you earn more than. Note that, at any point in time, you are better that
              someone and someone is also better that you. For this reason everyone can save and invest and the best time is now.</p>

